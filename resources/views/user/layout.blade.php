            $('.delete-data').click(function (e) {
                e.preventDefault();

                var id = $(this).data('id');
                // var path = e.currentTarget.getAttribute('href');
                // console.log(path);
                // alert(path);

                Swal.fire({
                    title: "Are you sure?",
                    text: "You won't be able to revert this!",
